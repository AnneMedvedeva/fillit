#include "libft.h"

char	*ft_strrev(char *s)
{
	size_t	i;
	size_t	j;
	char	temp;

	i = 0;
	while (s[i] != '\0')
		i++;
	j = -1;
	while (++j < --i)
	{
		temp = s[j];
		s[j] = s[i];
		s[i] = temp;
	}
	return (s);
}
